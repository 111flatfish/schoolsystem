                });
            }, 3000);
        });
        // 表单缩放
        $(".form #minimize").click(function () {
            $(".form").animate({
                width:"150px",
                height:"50px",
                left:"90%",
                marginLeft:"50px"
            });
            $(".form #maxmize").css("display","inline");
        });
        $(".form #maxmize").click(function () {
            $(".form").animate({
                width:"100%",
                height:"400px",
                left:"0",
                marginLeft:"0"
            });
            $(".form #maxmize").css("display","none");
        });
        // 表单验证
        $("#form").validator({
           fields:{
               name:"required;",
               phone:"required;integer(+0)",
               weixin:"integer",
               qq:"integer(+0)",
               from:"required",
               fromtxt:"required",
               major:"required"
           }
        });
        $("#form #selectFrom").click(function () {
            if($(this).val() == "other"){
                $(this).next("#selectFromTxt").show();
            }
        });



    });
